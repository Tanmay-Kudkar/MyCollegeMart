ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS skill_service_id BIGINT;
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS service_type VARCHAR(64);
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS subject VARCHAR(255);
