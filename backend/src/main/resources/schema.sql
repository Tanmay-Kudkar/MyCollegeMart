ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS description TEXT;
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS branch VARCHAR(255);
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS semester VARCHAR(16);
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS deadline VARCHAR(32);
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS total_amount DOUBLE PRECISION;
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS status VARCHAR(32);
ALTER TABLE IF EXISTS assignment_help_request ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ;
UPDATE assignment_help_request SET total_amount = 0 WHERE total_amount IS NULL;
UPDATE assignment_help_request SET status = 'SUBMITTED' WHERE status IS NULL;
UPDATE assignment_help_request SET created_at = CURRENT_TIMESTAMP WHERE created_at IS NULL;
ALTER TABLE IF EXISTS assignment_help_request ALTER COLUMN total_amount SET DEFAULT 0;
ALTER TABLE IF EXISTS assignment_help_request ALTER COLUMN status SET DEFAULT 'SUBMITTED';
ALTER TABLE IF EXISTS assignment_help_request ALTER COLUMN created_at SET DEFAULT CURRENT_TIMESTAMP;
CREATE INDEX IF NOT EXISTS idx_assignment_help_created_at ON assignment_help_request (created_at DESC);

